// GetContentStringFromTemplate е°Ҷ PromptTemplate иҪ¬дёәеӨҡиЎҢж–Үжң¬гҖӮ
//
// и°ғз”ЁжЁЎжқҝзҡ„ ToMessages() иҺ·еҸ–ж¶ҲжҒҜеҲ—иЎЁпјҢжӢјжҺҘжүҖжңүж¶ҲжҒҜзҡ„ж–Үжң¬еҶ…е®№пјҢз”ЁжҚўиЎҢз¬ҰиҝһжҺҘгҖӮ
//
// еҜ№еә” Python: TuneUtils.get_content_string_from_template(template)
//   "\n".join(msg.content for msg in template.to_messages())
func GetContentStringFromTemplate(tpl *prompt.PromptTemplate) string {
	messages, err := tpl.ToMessages()
	if err != nil || len(messages) == 0 {
		return ""
	}

	parts := make([]string, 0, len(messages))
	for _, msg := range messages {
		content := msg.GetContent()
		if content.IsText() {
			parts = append(parts, content.Text())
		}
	}
	return strings.Join(parts, "\n")
}

